import UIKit

extension UIFont {
    static func avenir20() -> UIFont? {
        return UIFont.init(name: "avanir", size: 20)
    }
    
    static func avenir26() -> UIFont? {
        return UIFont.init(name: "avanir", size: 26)
    }

}
